// Práctica numérica
// Realice un programa que solicite el ingreso de tres números
// enteros, y luego en cada caso informe si el número es par
// o impar.
// Para cada caso imprimir el resultado en pantalla.

const numero1 = parseInt(prompt('Ingrese el primer numero entero:\n'));

if(numero1 % 2 == 0){
    alert("Es un numero par");
}else{
    alert("Es un numero impar");
}


const numero2 = parseInt(prompt('Ingrese el segundo numero entero:\n'));

if(numero2 % 2 == 0){
    alert("Es un numero par");
}else{
    alert("Es un numero impar");
}


const numero3 = parseInt(prompt('Ingrese el tercer numero entero:\n'));

if(numero3 % 2 == 0){
    alert("Es un numero par");
}else{
    alert("Es un numero impar");
}